def A070030(n)
  ary =
  [0,0,0,0,16,176,1536,15424,147728,1448416,
   14060048,136947616,1332257856,12965578752,
   126169362176,1227776129152,11947846468608,
   116266505653888,1131418872918784,
   11010065269439104,107141489725900544,
   1042616896632882688]
  a =
  [6, 64, -200, -1000, 3016, 3488, -24256, 23776, 104168, -203408, -184704, 443392, 14336, -151296, 145920, -263424, 317440, 36864, -966656, 573440, 131072]
  cnt = 22
  return ary[0..n - 1] if n <= 22
  while cnt < n
    ary << (1..21).inject(0){|s, i| s + a[i - 1] * ary[-i]}
    cnt += 1
  end
  ary
end

